// Variables
#include <stdio.h>

int main()
{
    // Declaramos una variable
    int miNumero;
    // Inicializar los variables
    miNumero = 10;
    // Imprimir la variable.
    //%d - decimal
    printf("Mi numero es: %d", miNumero);

    // Declaramos e inicializamos la variable.
    int miNumero2 = 20, miNumero3 = 39;
    // Imprimir las variables.
    printf("\nMi numero 2 es: %d, \nMi numero 3 es: %d", miNumero2, miNumero3);

    return 0;
};