/* Variables en JavaScript */

// Declaración de variables
let miNumero = 10; // Variable de tipo número
let miTexto = "Hola, mundo!";
let miBooleano = true; // Variable de tipo booleano

// Imprimir variables en la consola
console.log(miNumero); // Imprime: 10
console.log(miTexto); // Imprime: Hola, mundo!
console.log(miBooleano); // Imprime: true
