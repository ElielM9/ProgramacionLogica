// Operadores aritmeticos en C#

// Suma 
var a = 3 + 4; 
Console.WriteLine($"Suma: {a}" ); 

// Resta 
var b = 6 - 2; 
Console.WriteLine($"Resta: {b}" ); 

// Multiplicacion 
var c = a * 2;
Console.WriteLine($"Producto: {c}" ); 

// Division 
var d = b / 2.5; 
Console.WriteLine($"Cociente: {d}" );

// Modulo 
var f = 9 % 2; 
Console.WriteLine($"Residuo: {f}"); 

// Potencia 
var e = Math.Pow(a, 2); 
Console.WriteLine($"Potencia: {e}" );

